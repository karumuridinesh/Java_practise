package com.Practice.Loops;

public class While_loop {

	public static void main(String[] args) {
		/* While loop 
		Syntax:
		while (condition) {
			System.out.println("while loop statement");
		}	
		*/
		int a = 1;
		while(a<10) {
			System.out.println("Given number is :" + a +" It is maller than 10");
			a++;
		}
		System.out.println("");
		System.out.println("while loop executed");
		
	}

}
